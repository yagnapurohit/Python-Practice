# Python Variables and Data Types Example

# Integer (whole numbers)
age = 16
print("Age:", age)
print("Type of age:", type(age))

# Float (decimal numbers)
height = 5.7
print("\nHeight:", height)
print("Type of height:", type(height))

# String (text)
name = "Alex"
print("\nName:", name)
print("Type of name:", type(name))

# Boolean (True or False)
is_student = True
print("\nIs Student:", is_student)
print("Type of is_student:", type(is_student))

# List (collection of values)
subjects = ["Math", "Science", "Python"]
print("\nSubjects:", subjects)
print("Type of subjects:", type(subjects))

# Dictionary (key-value pairs)
student_info = {
    "name": name,
    "age": age,
    "is_student": is_student
}
print("\nStudent Info:", student_info)
print("Type of student_info:", type(student_info))
